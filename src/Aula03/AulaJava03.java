package Aula03;

public class AulaJava03 {
    public static void main (String[] args) {
        String ingrediente01 = "frigideira";
        String ingrediente02 = "óleo";
        String ingrediente03 = "ovo";
        String ingrediente04 = "espátula";
        String ingrediente05 = "sal";
        String ingrediente06 = "pimenta";
        String ingrediente07 = "prato";


        System.out.println("Para fazer o " + ingrediente03 + "frito, siga os passos abaixo:");
        System.out.println("1)Pegue a " + ingrediente01);
        System.out.println("2)Coloque a " + ingrediente01 + " na primeira boca disponível");
        System.out.println("3) Adicione um pouco de " + ingrediente02 + " na " + ingrediente01 + ".");
        System.out.println("4) Ligue o fogo em temperatura média.");
        System.out.println("5) Quebre o " + ingrediente03 + " e coloque com cuidado na " + ingrediente01 + ".");
        System.out.println("6) Espere o " + ingrediente03 + " fritar por alguns minutos.");
        System.out.println("7) Use a " + ingrediente04 + " para virar o " + ingrediente03 + " (opcional, se quiser dos dois lados).");
        System.out.println("8) Tempere com " + ingrediente05 + " e " + ingrediente06 + " a gosto.");
        System.out.println("9) Quando estiver pronto, use a " + ingrediente04 + " para retirar o " + ingrediente03 + " da " + ingrediente01 + ".");
        System.out.println("10) Coloque o " + ingrediente03 + " frito no " + ingrediente07 + " e sirva.");
        System.out.println("Bom apetite!");
    }
}
